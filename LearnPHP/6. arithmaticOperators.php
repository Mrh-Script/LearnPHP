<?php

$a = 10;
$b = 3;

// Addition (+)
echo "Addition : " . ($a + $b);
echo "<br>";

// Subtraction (-)
echo "Subtraction : " . ($a - $b);
echo "<br>";

// Multiplication (*)
echo "Multiplication : " . ($a * $b);
echo "<br>";

// Exponentiation (**)
echo "Exponentiation : " . ($a ** $b);
echo "<br>";

// Division (/)
echo "Division : " . ($a / $b);
echo "<br>";

// Modulus / Remainder (%)
echo "Modulus : " . ($a % $b);
echo "<br><hr>";

// Pre-Increment (++a)
echo "Pre-Increment : " . (++$a);
echo "<br>";

// Post-Increment (a++)
echo "Post-Increment : " . ($a++);
echo "<br>";

// Pre-Decrement (--a)
echo "Pre-Decrement : " . (--$a);
echo "<br>";

// Post-Decrement (a--)
echo "Post-Decrement : " . ($a--);
echo "<br><hr>";

// Final value of a
echo "Final value of a : " . $a;

?>
